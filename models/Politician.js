class Politician {
  /**
   *
   * @param {string} name
   * @param {string} party
   * @param {string} riding
   * @param {number} yearElected
   * @param {string} imageUrl
   */
  constructor(name, party, riding, yearElected, imageUrl) {
    this.name = name
    this.party = party
    this.riding = riding
    this.yearElected = yearElected
    this.imageUrl = imageUrl
  }
}
