class User {
  constructor(
    email,
    firstname,
    lastname,
    password,
    postalCode,
    riding,
    categories
  ) {
    this.email = email
    this.firstname = firstname
    this.lastname = lastname
    this.password = password
    this.postalCode = postalCode
    this.riding = riding
    this.categories = categories
  }
}
